#!/bin/bash
# Generate the compact routing index for triangulate recurring rules.
# common-rules.md remains the sole source of truth.

set -eu

ROOT=$(git rev-parse --show-toplevel 2>/dev/null) || {
  echo "Error: not inside a git repository" >&2
  exit 2
}
SOURCE="${1:-$ROOT/skills/triangulate/common-rules.md}"
OUTPUT="${2:-$ROOT/skills/triangulate/common-rules.digest.md}"
MODE="${3:-write}"
TMP=$(mktemp)
trap 'rm -f "$TMP"' EXIT

[ -f "$SOURCE" ] || { echo "Error: missing $SOURCE" >&2; exit 2; }

{
  echo '<!-- Generated by hooks/generate-triangulate-rule-digest.sh; do not edit. -->'
  echo '# Triangulate Recurring Rule Digest'
  echo
  echo 'Read this routing index first. Match the diff against pattern names, then extract only the triggered rows from `common-rules.md` with:'
  echo
  echo '```bash'
  echo "rg -n '^\\| (R1|R3|RS2|RT4) \\|' skills/triangulate/common-rules.md"
  echo '```'
  echo
  echo "Also extract a rule's Extended obligations section when the selected row points to it. Do not read the full rules file unless targeted extraction is inconclusive."
  echo
  echo '| ID | Pattern | Severity if missed |'
  echo '|---|---|---|'
  awk -F'[|]' '
    /^\| (R[0-9]+|RS[0-9]+|RT[0-9]+) [|]/ {
      # Procedure cells may contain literal pipes (for example regexes such
      # as /\|/g). The ID and pattern are the first two cells, while severity
      # is always the final non-empty cell before the trailing row pipe.
      id=$2; pattern=$3; severity=$(NF-1)
      gsub(/^[[:space:]]+|[[:space:]]+$/, "", id)
      gsub(/^[[:space:]]+|[[:space:]]+$/, "", pattern)
      gsub(/^[[:space:]]+|[[:space:]]+$/, "", severity)
      print "| " id " | " pattern " | " severity " |"
    }
  ' "$SOURCE"
} > "$TMP"

[ "$(grep -Ec '^\| (R[0-9]+|RS[0-9]+|RT[0-9]+) \|' "$TMP")" -gt 0 ] || {
  echo 'Error: no recurring-rule rows found' >&2
  exit 2
}

if [ "$MODE" = "--check" ]; then
  if ! cmp -s "$TMP" "$OUTPUT"; then
    echo "Triangulate rule digest is stale. Run: bash hooks/generate-triangulate-rule-digest.sh" >&2
    exit 1
  fi
  echo 'Triangulate rule digest is up to date.'
elif [ "$MODE" = "write" ]; then
  cp "$TMP" "$OUTPUT"
else
  echo "Usage: $0 [source] [output] [write|--check]" >&2
  exit 2
fi
